// -- && and = Somente se se duas informaçõe forem verdadeira, significa que é verdadeira           
console.log(5 > 3 && 3 == 2)
console.log(true && true)

// -- || or = Se uma das duas informações forem verdadeiram significa que é verdadeira
console.log(5 > 3 || 3 == 2)
console.log(false || true)

 //  --!  ~ = Senal de negação//
console.log(!true)
console.log(!true && true)
